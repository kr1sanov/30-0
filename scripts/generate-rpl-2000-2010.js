// For each club-season combo, ensure at least MIN_PLAYERS_PER_CLUB players

const MIN_PLAYERS_PER_CLUB = 22;

// Russian first/last name pools for squad fillers
const firstNames = ["Александр","Дмитрий","Сергей","Андрей","Алексей","Максим","Иван","Евгений","Михаил","Владимир","Денис","Николай","Павел","Виталий","Роман","Олег","Игорь","Юрий","Артём","Кирилл","Антон","Вадим","Константин","Валерий","Тимур","Руслан","Алексей","Виктор","Пётр","Михаил"];
const lastNames = ["Иванов","Смирнов","Кузнецов","Попов","Васильев","Петров","Соколов","Михайлов","Новиков","Фёдоров","Морозов","Волков","Алексеев","Лебедев","Семёнов","Егоров","Павлов","Козлов","Степанов","Николаев","Орлов","Андреев","Макаров","Никитин","Захаров","Зайцев","Соловьёв","Борисов","Яковлев","Григорьев","Романов","Воробьёв","Сергеев","Кузьмин","Фролов","Александров","Дмитриев","Королёв","Гусев","Киселёв"];
const positions = [["ВР",[]],["ЦЗ",[]],["ЦЗ",[]],["ПЗ",[]],["ЛЗ",[]],["ОП",[]],["ЦП",[]],["ЦП",[]],["АП",[]],["ПП",[]],["ЛП",[]],["ЛВ",[]],["ПВ",[]],["НП",[]],["ЦН",[]]];
const nationalities = ["Россия","Россия","Россия","Россия","Россия","Россия","Украина","Беларусь"];

// Seed for reproducibility
let seed = 42;
function seededRandom() {
  seed = (seed * 16807 + 0) % 2147483647;
  return (seed - 1) / 2147483646;
}

function generateFillerPlayer(club, season, index) {
  const posIdx = index % positions.length;
  const [mainPos, otherPos] = positions[posIdx];
  const natIdx = Math.floor(seededRandom() * nationalities.length);
  const nationality = nationalities[natIdx];
  const fnIdx = Math.floor(seededRandom() * firstNames.length);
  const lnIdx = Math.floor(seededRandom() * lastNames.length);
  const firstName = firstNames[fnIdx];
  const lastName = lastNames[lnIdx];
  const fullName = `${firstName} ${lastName}`;
  
  // Rating: 58-68 range for squad fillers
  const baseRating = 58 + Math.floor(seededRandom() * 11);
  
  return [fullName, lastName, nationality, mainPos, otherPos, [[season, club, baseRating]]];
}


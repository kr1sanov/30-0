const playerMap = new Map();

for (const def of playerDefs) {
  const [fullName, lastName, nationality, mainPosition, otherPositions, seasons] = def;
  // Only include seasons 2000-2010
  const filteredSeasons = seasons.filter(([s]) => s >= 2000 && s <= 2010);
  if (filteredSeasons.length === 0) continue;
  
  const key = `${fullName}__${nationality}__${mainPosition}`;
  
  if (!playerMap.has(key)) {
    playerMap.set(key, {
      fullName,
      lastName,
      nationality,
      mainPosition,
      otherPositions,
      seasons: []
    });
  }
  
  const player = playerMap.get(key);
  for (const [season, club, rating] of filteredSeasons) {
    // Check for duplicate season entries
    const existing = player.seasons.find(s => s.season === season);
    if (existing) {
      // Update if higher rating
      if (rating > existing.rating) {
        existing.club = club;
        existing.rating = rating;
      }
    } else {
      player.seasons.push({ season, club, rating });
    }
  }
}

// Count current players per club per season
const clubSeasonCounts = {};
for (const player of playerMap.values()) {
  for (const s of player.seasons) {
    const key = `${s.season}__${s.club}`;
    clubSeasonCounts[key] = (clubSeasonCounts[key] || 0) + 1;
  }
}

// Generate fillers for under-populated club-seasons
let fillerIdx = 0;
for (const [seasonStr, clubs] of Object.entries(seasonClubs)) {
  const season = parseInt(seasonStr);
  for (const club of clubs) {
    const key = `${season}__${club}`;
    const current = clubSeasonCounts[key] || 0;
    const needed = MIN_PLAYERS_PER_CLUB - current;
    
    if (needed > 0) {
      for (let i = 0; i < needed; i++) {
        const filler = generateFillerPlayer(club, season, fillerIdx++);
        const [fullName, lastName, nationality, mainPosition, otherPositions, seasons] = filler;
        const pKey = `${fullName}__${nationality}__${mainPosition}__filler`;
        
        if (!playerMap.has(pKey)) {
          playerMap.set(pKey, {
            fullName,
            lastName,
            nationality,
            mainPosition,
            otherPositions,
            seasons: []
          });
        }
        playerMap.get(pKey).seasons.push({ season, club, rating: seasons[0][2] });
      }
    }
  }
}

// ============================================================
// GENERATE OUTPUT
// ============================================================

const output = [];

for (const player of playerMap.values()) {
  // Sort seasons
  player.seasons.sort((a, b) => a.season - b.season);
  
  // Find prime rating and season
  let primeRating = 0;
  let primeSeason = '';
  for (const s of player.seasons) {
    if (s.rating > primeRating) {
      primeRating = s.rating;
      primeSeason = String(s.season);
    }
  }
  
  for (const s of player.seasons) {
    output.push({
      fullName: player.fullName,
      lastName: player.lastName,
      nationality: player.nationality,
      club: s.club,
      season: String(s.season),
      mainPosition: player.mainPosition,
      otherPositions: player.otherPositions,
      rating: s.rating,
      primeRating,
      primeSeason
    });
  }
}

// Sort by season, then club, then lastName
output.sort((a, b) => {
  if (a.season !== b.season) return a.season.localeCompare(b.season);
  if (a.club !== b.club) return a.club.localeCompare(b.club);
  return a.lastName.localeCompare(b.lastName);
});

// Filter: only keep records where club is valid for that season
const validClubsBySeason = {};
for (const [s, clubs] of Object.entries(seasonClubs)) {
  validClubsBySeason[s] = new Set(clubs);
}

const filteredOutput = output.filter(r => {
  const validClubs = validClubsBySeason[r.season];
  return validClubs && validClubs.has(r.club);
});

// Recalculate primeRating/primeSeason after filtering
// Group by player key, recalculate prime
const playerRecords = new Map();
for (const r of filteredOutput) {
  const pKey = `${r.fullName}__${r.nationality}__${r.mainPosition}`;
  if (!playerRecords.has(pKey)) playerRecords.set(pKey, []);
  playerRecords.get(pKey).push(r);
}

for (const records of playerRecords.values()) {
  let primeRating = 0;
  let primeSeason = '';
  for (const r of records) {
    if (r.rating > primeRating) {
      primeRating = r.rating;
      primeSeason = r.season;
    }
  }
  for (const r of records) {
    r.primeRating = primeRating;
    r.primeSeason = primeSeason;
  }
}


function careerArc(startSeason, endSeason, startRating, peakRating, peakSeason, endRating) {
  const seasons = {};
  for (let s = startSeason; s <= endSeason; s++) {
    if (s <= peakSeason) {
      const progress = (s - startSeason) / (peakSeason - startSeason || 1);
      seasons[s] = Math.round(startRating + (peakRating - startRating) * progress);
    } else {
      const decline = (s - peakSeason) / (endSeason - peakSeason || 1);
      seasons[s] = Math.round(peakRating + (endRating - peakRating) * decline);
    }
  }
  return seasons;
}

function flatArc(startSeason, endSeason, rating) {
  const seasons = {};
  for (let s = startSeason; s <= endSeason; s++) seasons[s] = rating;
  return seasons;
}

function player(fullName, lastName, nationality, mainPosition, otherPositions, ratingBySeason, clubBySeason) {
  const entries = Object.entries(ratingBySeason).sort((a, b) => b[1] - a[1] || parseInt(a[0]) - parseInt(b[0]));
  const primeSeason = entries[0];
  return {
    fullName, lastName, nationality, mainPosition, otherPositions,
    ratingBySeason, clubBySeason,
    primeRating: primeSeason[1],
    primeSeason: String(primeSeason[0])
  };
}

function toRecords(p) {
  const records = [];
  for (const season of Object.keys(p.ratingBySeason)) {
    const s = String(season);
    if (!p.clubBySeason[season]) continue;
    records.push({
      fullName: p.fullName, lastName: p.lastName, nationality: p.nationality,
      club: p.clubBySeason[season], season: s, mainPosition: p.mainPosition,
      otherPositions: p.otherPositions, rating: p.ratingBySeason[season],
      primeRating: p.primeRating, primeSeason: p.primeSeason
    });
  }
  return records;
}

const allPlayers = [];

// Helper to add a player with a simple club arc
function p(fullName, lastName, nat, pos, otherPos, startS, endS, startR, peakR, peakS, endR, club) {
  allPlayers.push(player(fullName, lastName, nat, pos, otherPos,
    careerArc(startS, endS, startR, peakR, peakS, endR),
    flatArc(startS, endS, club)));
}

// Helper: player with specific club assignments per season
function pc(fullName, lastName, nat, pos, otherPos, ratings, clubs) {
  allPlayers.push(player(fullName, lastName, nat, pos, otherPos, ratings, clubs));
}

// Helper: flat rating player
function pf(fullName, lastName, nat, pos, otherPos, startS, endS, rating, club) {
  allPlayers.push(player(fullName, lastName, nat, pos, otherPos,
    flatArc(startS, endS, rating), flatArc(startS, endS, club)));
}


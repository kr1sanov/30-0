// ============================================================
// HELPER: check if a startYear is in any of the ranges
// ============================================================
function isInRanges(year: number, ranges: [number, number][]): boolean {
  return ranges.some(([start, end]) => year >= start && year <= end);
}

// Helper: generate compatible secondary positions
function getOtherPositions(posKey: PosKey): string | null {
  const positionCompat: Record<PosKey, string[]> = {
    GK: [],
    CB: ['ЦЗ'],
    RB: ['ПЗ', 'ЦЗ'],
    LB: ['ЛЗ', 'ЦЗ'],
    RWB: ['ПФЗ', 'ПЗ'],
    LWB: ['ЛФЗ', 'ЛЗ'],
    CDM: ['ОП', 'ЦП'],
    CM: ['ЦП', 'ОП', 'АП'],
    CAM: ['АП', 'ЦП', 'НП'],
    LM: ['ЛП', 'ЛВ'],
    RM: ['ПП', 'ПВ'],
    LW: ['ЛВ', 'ЛП', 'НП'],
    RW: ['ПВ', 'ПП', 'НП'],
    ST: ['НП', 'ЦН'],
    CF: ['ЦН', 'НП', 'АП'],
  };
  const compat = positionCompat[posKey];
  if (compat.length === 0) return null;
  const subset = compat.slice(0, Math.min(2, compat.length));
  return subset.join(',');
}

// Seeded pseudo-random for deterministic output
let seed = 42;
function seededRandom(): number {
  seed = (seed * 16807 + 0) % 2147483647;
  return (seed - 1) / 2147483646;
}


  console.log(`  Seasons:        ${totalSeasons}`);
  console.log(`  ClubSeasons:    ${totalClubSeasons}`);
  console.log(`  Players:        ${totalPlayers}`);
  console.log(`  PlayerSeasons:  ${totalPlayerSeasons}`);
